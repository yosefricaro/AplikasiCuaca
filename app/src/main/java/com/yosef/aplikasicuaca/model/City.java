package com.yosef.aplikasicuaca.model;

public class City {
    private String name;
    private Coord coord;
    private String country;
    private String population;

    public String getName() {
        return name;
    }

    public Coord getCoord() {
        return coord;
    }

    public String getCountry() {
        return country;
    }

    public String getPopulation() {
        return population;
    }
}
